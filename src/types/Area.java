package types;

public class Area {
    private String area;
    private String name;

    public Area(String area, String name) {
        this.area = area;
        this.name = name;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Area: " + area + ", Name: " + name;
    }
}
